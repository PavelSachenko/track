class User extends Authenticatable
{
    use HasApiTokens, HasFactory, Notifiable;

    const TYPE_AGENT = 1;
    const TYPE_AGENCY = 2;

    const USER_TYPES = [
        self::TYPE_AGENT => 'agent',
        self::TYPE_AGENCY => 'agency',
    ];

    const TYPE_TABLES = [
        self::TYPE_AGENT => 'agents',
        self::TYPE_AGENCY => 'agencies',
    ];

    /**
     * The attributes that are mass assignable.
     *
     * @var array<int, string>
     */
    protected $fillable = [
        'email',
        'password',
    ];

    /**
     * The attributes that should be hidden for serialization.
     *
     * @var array<int, string>
     */
    protected $hidden = [
        'password',
        'remember_token',
    ];

    /**
     * The attributes that should be cast.
     *
     * @var array<string, string>
     */
    protected $casts = [
        'email_verified_at' => 'datetime',
    ];


    /*
    |--------------------------------------------------------------------------
    | Relationship
    |--------------------------------------------------------------------------
    */
    public function agent(): \Illuminate\Database\Eloquent\Relations\HasOne
    {
        return $this->hasOne(Agent::class, 'user_id', 'id');
    }

    public function agency(): \Illuminate\Database\Eloquent\Relations\HasOne
    {
        return $this->hasOne(Agency::class, 'user_id');
    }

}
